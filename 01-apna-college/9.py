#Comparison Operator

print(3 > 2)
print(3 < 2)
print(3 >= 2)
print(3 <= 2)
print(3 == 2)
print(3 == 3)
print(3 != 2)
print(3 != 3)

#Logical Operator

# or -> (atleast one is true)
# and -> (both are true)
# not -> (reverses any value)

number = 2
print(number > 3 and number > 1)
print(number > 3 or number > 1)
print(not number > 3)
print(not number < 3)